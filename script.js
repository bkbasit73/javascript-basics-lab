//Demonstrate Data Types
let stringValue = "Hello, World!";
let numericValue = 42;
let booleanValue = false;
let fruits = ["apple", "banana", "cherry"];
let person = { firstName: "mark", lastName: "angel" };


console.log("String:", stringValue);
console.log("Number:", numericValue);
console.log("Boolean:", booleanValue);
console.log("Array:", fruits);
console.log("Object:", person);

// Use basic operators
let x = 8;
let y = 5;

console.log("Addition:", x + y);
console.log("Subtraction:", x - y);
console.log("Multiplication:", x * y);
console.log("Division:", x / y);

console.log("Greater than:", x > y);
console.log("Less than:", x < y);
console.log("Equal to:", x === y);
console.log("Not equal:", x !== y);

console.log("AND:", true && true);
console.log("OR:", true || false);
console.log("NOT:", !true);